"""
1.打印一个乘法表达式
2.打印每行，内循环。内循环与外循环联动，控制每列的个数。
3.打印9行，外循环控制

注意，内循环中加入if判断使结构完美。
"""

i = 1
while i <= 9:
    j = 1
    while j <= i:
        '''
        if ((i == 3) and (j == 3)) or ((i == 4) and (j == 3)):
            print(end=" ")
        多余，'\t'完全可以完美解决
        '''
        print(f"{j} * {i} = {i * j}", end="\t")
        j += 1
    print()
    i += 1